package arraydemo;

public class demo {
// array is a special type of container which can store same type of multiple values

	public static void main(String[] args) {

		// int a = 10;

		int a[] = { 10, 20, 30, 40 };
		
		// size= 4
		
		// index = 0-3
		
		// index = size-1

		for (int vl : a) {
			System.out.println(vl);
		}

	}

}
